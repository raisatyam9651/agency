    <h2>1. We start with the offer, not the channel</h2>
    <p>Most campaigns fail because the offer is generic. We pressure-test the offer first: is it specific, is it valuable, is it credible? The right offer to the wrong audience still fails; the right offer to the right audience converts at 2-3x industry benchmarks.</p>

    <h2>2. We map every step of the funnel</h2>
    <p>Cold traffic needs different creative than warm retargeting. Bottom-of-funnel traffic needs different CTAs than top-of-funnel. We map each segment separately, then build creative and copy for each. See our guide on <a href="<?php echo $base_path; ?>blog/how-do-i-optimize-my-homepage">homepage optimization</a> for the conversion principles that apply to every page.</p>

    <h2>3. We write ad copy like landing page copy</h2>
    <p>Ad copy should match the language of the search. Long-tail <a href="<?php echo $base_path; ?>blog/how-do-i-find-low-competition-keywords">low-competition keyword</a> targeting means long-tail ad copy. Specificity wins — generic copy gets ignored.</p>

    <h2>4. We optimize landing pages, not just ads</h2>
    <p>The biggest conversion-rate gains usually come from landing page improvements, not from tweaking bids. We A/B test hero copy, CTAs, social proof placement, and form length. A 50% lift in landing page conversion is common.</p>

    <h2>5. We scale what works, kill what doesn't</h2>
    <p>By week 4, we know which audiences and creatives are performing. We push budget to winners, cut losers, and expand into lookalike audiences of the converters. This is where most agencies stop — we keep optimizing through the entire flight.</p>

