  <article class="container post-body">
    <p>"How many pages should my website have?" is one of the most-asked SEO questions, and the honest answer is: it depends on what your site needs to do. But there is a framework that gives you the right number for your business — and a way to recognize when you've gone too far in either direction.</p>

    <h2>Start with the three categories, not a count</h2>
    <p>Every page on your site should fall into one of three buckets:</p>
    <ul>
      <li><strong>Money pages</strong> — the pages that exist to convert visitors into leads or customers. Product pages, service pages, pricing, contact. Usually 5-30 for most businesses.</li>
      <li><strong>Supporting content</strong> — blog posts, guides, comparison pages that build topical authority and bring in organic traffic. This is where the volume lives.</li>
      <li><strong>Trust assets</strong> — about, team, case studies, certifications, contact, legal. 5-15 pages that make buyers comfortable.</li>
    </ul>
    <p>Once you have these buckets clear, the count follows from your niche, not from a generic target.</p>

    <h2>Rough sizing by business type</h2>
    <p>These are starting points, not rules:</p>
    <ul>
      <li><strong>Local single-service business</strong> — 15-30 pages. Homepage, 5-10 service-area pages, 10-15 supporting blog posts, trust assets.</li>
      <li><strong>SaaS / software</strong> — 50-200 pages. Feature pages, integration pages, comparison pages ("vs competitor"), docs.</li>
      <li><strong>E-commerce</strong> — 100-1,000+ pages. Product, category, blog. Scale is the point here.</li>
      <li><strong>SEO-driven publisher</strong> — 200-1,000+ pages. Almost entirely supporting content clustered around money pages.</li>
    </ul>

    <h2>The diminishing-returns rule</h2>
    <p>Each new page should earn its keep. A simple test: would you write a 200-word blurb for it? If the topic doesn't deserve even that, the page is hurting you. Audit quarterly for pages that get zero organic traffic after 6 months and either improve them dramatically, merge them, or noindex them.</p>

    <h2>Watch out for index bloat</h2>
    <p>The mistake on the other side is publishing 10,000 thin AI-generated pages to "win at SEO." Google's Helpful Content system explicitly filters this pattern. The size of your site should be the natural consequence of covering your topic well, not a vanity metric.</p>

    <h2>How to decide what to publish next</h2>
    <p>For a small site, build out the money pages and trust assets first. For a larger site, identify the topical gaps your competitors cover that you don't — those are your next pages. See <a href="<?php echo $base_path; ?>blog/how-do-i-optimize-my-homepage">our homepage optimization guide</a> for the home-page side of the equation.</p>
